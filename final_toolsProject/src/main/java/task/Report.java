package task;

public class Report {
	private int earnedMoney;
	private int numOfCompletedOrders;
	private int numOfCancelledOrders;
	
	
	
	public int getEarnedMoney() {
		return earnedMoney;
	}
	public void setEarnedMoney(int earnedMoney) {
		this.earnedMoney = earnedMoney;
	}
	public int getNumOfCompletedOrders() {
		return numOfCompletedOrders;
	}
	public void setNumOfCompletedOrders(int numOfCompletedOrders) {
		this.numOfCompletedOrders = numOfCompletedOrders;
	}
	public int getNumOfCancelledOrders() {
		return numOfCancelledOrders;
	}
	public void setNumOfCancelledOrders(int numOfCancelledOrders) {
		this.numOfCancelledOrders = numOfCancelledOrders;
	}
	
	
	
	

}
